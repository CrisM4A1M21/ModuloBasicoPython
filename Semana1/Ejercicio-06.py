"""Tipo de datos trascendentales (listas, tuplas, set, diccionarios)"""


var_1 = [1,"Luz","Calle las Almendras",1992]

print("El valor de mi variable lista es :\n{}".format(var_1))

var_2 = (40, 80, 170, 2000)

print("El valor de mi variable tupla es :\n{}".format(var_2))

var_set = set(["Bienvenidos",50,40.78, False])
print("El valor de mi variable set es :\n{}".format(var_set))

diccionario = {"nombre":"Juan", "edad":30, "dni":12345678}
print("El valor de mi variable diccionario es :\n{}".format(diccionario))